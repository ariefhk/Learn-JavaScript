//Perbandingan
// Equal (==)
var angka = 50;
console.log(angka == 50);
console.log(angka == 15);

// Not Equal (!=)
var hobby = "coding";
console.log(hobby != "game");
console.log(hobby != "mancing");

// Strict Equal (===)
var angka = 21;
console.log(angka == "21");
console.log(angka === "21");
console.log(angka === 21);

// Strict not Equal (===)
var angka = 18;
console.log(angka != "18");
console.log(angka !== "18");
console.log(angka !== 18);

// Kurang dari & Lebih Dari (<, >, <=, >=)
var angka = 15;
console.log(angka < 20);
console.log(angka > 15);
console.log(angka >= 15);
console.log(angka <= 20);

// Kondisional
// OR
console.log(true || true);
console.log(true || false);
console.log(true || false || false);
console.log(false || false);

// AND
console.log(true && true);
console.log(true && false);
console.log(false && false);
console.log(false && true && true);
console.log(true && true && true);
